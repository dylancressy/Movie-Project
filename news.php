					<b>‘American Idol’ Producer Core Media Group Files for Chapter 11 Bankruptcy</b>
					<br> 
					Core Media Group, home of 19 Entertainment’s “American Idol” and “So You Think You Can Dance,” was expected to file Thursday for Chapter 11 bankruptcy protection in New York.
					Core was part of the joint venture that 21st Century Fox and Apollo Global Management created in 2014 with the merger of Apollo’s Endemol and Core with Fox’s Shine Group. But Core continued to operate under a separate capital structure and was never integrated into what became Endemol Shine Group. Moreover, Fox was shielded from liability for 
					Core’s debts as part of the merger agreement as the company’s deteriorating financial condition was evident at the time of the deal. Core was known to be shouldering at least $300 million in debt as of last year but the total amount to be restructured was not immediately clear.
					The bankruptcy filing is supported by two of Core’s key lenders, Crestview Partners and Tennenbaum Capital, which will assume ownership of the company from Fox and Apollo following the completion of the Chapter 11 process. Discussions about the Chapter 11 filing have been under way with lenders for about a year, a Core rep said.
					Core has hobbled by debt problems for more than two years.
